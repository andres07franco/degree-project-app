    public void iniciar() {
        cbxciudad.removeAllItems();
        try {

            List<Ciudad> listaCiudades = (List<Ciudad>) Model.getInstance().obtenerListado("obtenerCiudades", "%");

            for (Ciudad obj : listaCiudades) {
                cbxciudad.addItem(obj);

            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

        txtnombre.requestFocus();
    }

