    public Empresa getEmpresa() {

        Empresa emp = new Empresa();

        emp.setNombre(txtnombre.getText());
        emp.setNit(Integer.parseInt(txtnit.getText()));
        emp.setCiudad((Ciudad) cbxciudad.getSelectedItem());
        emp.setDireccion(txtdireccion.getText());
        emp.setEmail(txtemail.getText());
        emp.setPropietario(txtpropietario.getText());
        emp.setTelefono(txttelefono.getText());
        emp.setLicencia(txtlicencia.getText());
        emp.setEstado(interfaces.Constantes.ESTADO_EMPRESA_ACTIVO+"");


        return emp;
    }

    public boolean validar() {
        
        if (txtnombre.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el NOMBRE de la EMPRESA", "Se ha producido un inconveniente", JOptionPane.ERROR_MESSAGE);
            txtnombre.requestFocus();
            return false;
        } else if (txtnit.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el NIT de la EMPRESA", "Se ha producido un inconveniente", JOptionPane.ERROR_MESSAGE);
            txtnit.requestFocus();
            return false;
        } else if (txtnit.getText().length()<8) {
            JOptionPane.showMessageDialog(null, "El NIT de la EMPRESA debe tener almenos 8 digitos", "Se ha producido un inconveniente", JOptionPane.ERROR_MESSAGE);
            txtnit.requestFocus();
            return false;
        }else if (txtdireccion.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese la DIRECCIÓN de la EMPRESA", "Se ha producido un inconveniente", JOptionPane.ERROR_MESSAGE);
            txtdireccion.requestFocus();
            return false;
        } else if (txtemail.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el E-MAL de la EMPRESA", "Se ha producido un inconveniente", JOptionPane.ERROR_MESSAGE);
            txtemail.requestFocus();
            return false;
        }  else if (!Validaciones.esEmail(txtemail.getText())) {
            JOptionPane.showMessageDialog(null, "Digite un E-MAIL valido porfavor");
            txtemail.requestFocus();
            return false;
        }else if (txtpropietario.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el PROPIETARIO de la EMPRESA", "Se ha producido un inconveniente", JOptionPane.ERROR_MESSAGE);
            txtpropietario.requestFocus();
            return false;
        } else if (txttelefono.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el TELEFONO de la EMPRESA", "Se ha producido un inconveniente", JOptionPane.ERROR_MESSAGE);
            txttelefono.requestFocus();
            return false;
        }else if (txttelefono.getText().length()<7) {
            JOptionPane.showMessageDialog(null, "El TELEFONO de la EMPRESA debe tener almeos 7 digitos", "Se ha producido un inconveniente", JOptionPane.ERROR_MESSAGE);
            txttelefono.requestFocus();
            return false;
        } else if (txtlicencia.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese la LICENCIA asignada a la EMPRESA", "Se ha producido un inconveniente", JOptionPane.ERROR_MESSAGE);
            txtlicencia.requestFocus();
            return true;
        }
        return true;
    }

